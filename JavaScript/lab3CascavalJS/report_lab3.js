/*
 1. Какие методы массивов можно использовать для обработки объектов в JavaScript?
    - map(): преобразует каждый элемент массива и возвращает новый массив.
    - filter(): отбирает элементы по условию и возвращает новый массив.
    - reduce(): аккумулирует результат, например, сумму или объект-счетчик.
    - find(): находит первый элемент, соответствующий условию.
    - some(): проверяет, есть ли хотя бы один элемент, подходящий под условие.
    - every(): проверяет, соответствуют ли все элементы условию.
*/

/*
 2. Как сравнивать даты в строковом формате в JavaScript?
    - Для надежного сравнения следует преобразовать строку в объект Date,
      например new Date("2024-03-01T00:00:00"), и сравнивать численные значения.
    - Прямое сравнение строк работает только для формата YYYY-MM-DD, но лучше
      использовать Date для поддержки более сложных операций.
*/

/*
 3. В чем разница между map(), filter() и reduce() при работе с массивами объектов?
    - map() возвращает новый массив с преобразованными элементами.
    - filter() возвращает новый массив, содержащий только элементы, прошедшие условие.
    - reduce() возвращает одно значение, полученное из всех элементов массива.
*/

