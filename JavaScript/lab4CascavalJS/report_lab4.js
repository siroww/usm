/*
 1. Какое значение имеет this в методах класса?
    - В методах класса this ссылается на экземпляр текущего объекта,
      на котором был вызван метод.
    - Внутри метода класса this позволяет получить доступ к полям и другим методам объекта.
*/

/*
 2. Как работает модификатор доступа # в JavaScript?
    - Символ # делает свойство или метод приватным.
    - Приватные поля доступны только внутри класса и не видны извне.
    - Попытка обратиться к #полю извне вызывает ошибку синтаксиса.
*/

/*
 3. В чем разница между классами и функциями-конструкторами?
    - Классы предоставляют более ясный синтаксис для объявления объектов и наследования.
    - Функции-конструкторы используют прототипы напрямую и работают с оператором new.
    - Под капотом классы тоже используют прототипы, но синтаксис более декларативный.
*/

